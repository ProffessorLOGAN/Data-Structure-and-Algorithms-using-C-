#include <iostream>

using namespace std;

int main()
{

    int a, b;
    cout << "input value for a:\n"; /* '<<' this is called insertion operator */
    cin >> a;                       /* '>>' this is called extraction operator */
    cout << "input value for b:\n";
    cin >> b;

    cout << "the value of a is : " << a<<"\n";
    cout << "the value of b is : " << b<<"\n";
}
